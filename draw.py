def scanline(screen, zbuffer, color, x0, x1, y, z0, z1):
    if(x0 > x1):
        scanline(screen, zbuffer, color, x1, x0, y, z1, z0)
        return
    if(x0 == x1):
        return
    x = x0
    z = z0
    z_slope = (z1 - z0) / (x1 - x0 + 1)
    while(x <= x1):
        plot(screen, zbuffer, color, x, y, z)
        x += 1
        z += z_slope

