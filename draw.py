def scanline_convert(polygons, n, screen, zbuffer ):
    '''
    For each polly:
        - Find Bottom, Top, Middle vertices
            * Just find the min, max, and mid y-vals and their respective points
        - Initial y value is Bottom-y (B_y), final y value is Top-y (T_y)
        - For each y value to cycle through, draw horizontal lines from x0 to x1
            * The initial x0 and x1 value will always be Bottom-x (B_x)
            * We need the change of x over every y value (how much x must change over a given step)
                ~ This is the same thing as dx/dy (dx), or the inverse slope
                ~ We also need two seperate dx values for the change in both x0 and x1 (dx0, dx1)
            * dx1 will change after y reaches Middle-y (M_y)
                ~ We can just have a second dx1 value (dx1_i, dx1_f)
            * To find dx0, dx1_i, and dx1_f, use these:
                ~ dx0 = (x_T - x_B)/(y_T - y_B)
                ~ dx1_i = (x_M - x_B)/(y_M - y_B)
                ~ dx1_f = (x_T - x_M)/(y_T - y_M)
            * Because dx will prob not be an int, our x value cannot be one either

                        ~~~ EZ PZ ~~~
    '''
    # find Bottom, Top, Middle
    color = rand_color()
    points = [polygons[n],polygons[n+1],polygons[n+2]]
    points.sort(key=my_key,reverse=True)
    y_T = points[0][1]; y_M = points[1][1]; y_B = points[2][1]
    x_T = points[0][0]; x_M = points[1][0]; x_B = points[2][0]
    z_T = points[0][2]; z_M = points[1][2]; z_B = points[2][2]
    dx0 = (x_T - x_B)/(y_T - y_B)
    dz0 = (z_T - z_B)/(y_T - y_B)
    dx1_i = dx1_f = 0
    dz1_i = dz1_f = 0
    if (abs(y_M - y_B) > .001):
        dx1_i = (x_M - x_B)/(y_M - y_B)
        dz1_i = (z_M - z_B)/(y_M - y_B)
    if (abs(y_T - y_M) > .001):
        dx1_f = (x_T - x_M)/(y_T - y_M)
        dz1_f = (z_T - z_M)/(y_T - y_M)
    x0 = x1 = x_B
    z0 = z1 = z_B
    y = int(y_B)
    while y < y_T:
        draw_line(int(x0), y, z0, int(x1), y, z1, screen, zbuffer, color)
        x0 += dx0
        x1 += dx1_i
        z0 += dz0
        z1 += dz1_i
        if y == int(y_M):
            dx1_i = dx1_f
            dz1_i = dz1_f
            dz = z_M
            x1 = x_M
        y+=1



